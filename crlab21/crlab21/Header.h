#pragma once

/*
    Задача:
    Дан стек, реализованный как связный список.

    Требуется:
    - добавить элемент D в стек
    - вывести адрес новой вершины (P2)

    Условия:
    - стек реализован через указатели (Node)
    - вершина стека — первый элемент
    - для пустого стека top = nullptr
    - ввод данных с клавиатуры
    - должен быть стандартный набор операций:
        push (добавление)
        pop (удаление) — можно не использовать, но должен быть
        вывод элементов
*/

/*
    Узел стека
*/
class Node
{
public:
    int Data;       // значение элемента
    Node* Next;     // указатель на следующий узел

    Node(int Value);
};

/*
    Класс стека
*/
class Stack
{
private:
    Node* Top; // вершина стека

public:
    Stack();

    // Добавление элемента в стек
    void Push(int Value);

    // Удаление элемента (необязательно используется, но по условию должен быть)
    void Pop();

    // Вывод элементов
    void Print() const;

    // Вывод адреса вершины
    void PrintAddress() const;

    // Получить значение вершины
    int GetTop() const;

    // Проверка на пустоту
    bool IsEmpty() const;
};