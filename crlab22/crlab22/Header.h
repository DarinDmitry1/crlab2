#pragma once

#include <iostream>

/*
    Задача:
    Реализовать очередь как цепочку связанных узлов.

    Требуется:
    1. Описать тип очереди с указателями на начало и конец.
    2. Реализовать стандартные операции:
       - добавление элемента в конец очереди
       - удаление элемента из начала очереди
       - вывод элементов очереди
    3. С помощью функции Dequeue извлечь 5 первых элементов
       и вывести их значения.
    4. Вывести адреса начала и конца полученной очереди.
       Если очередь пуста, оба адреса должны быть nullptr.

    Условия:
    - ввод данных выполняется с клавиатуры
    - очередь хранит целые числа
*/

/*
    Узел очереди
*/
struct PNode
{
    int Data;
    PNode* Next;
};

/*
    Очередь:
    Head — начало
    Tail — конец
*/
struct TQueue
{
    PNode* Head;
    PNode* Tail;

    TQueue();
};

/*
    Добавляет элемент Value в конец очереди Q
*/
void Enqueue(TQueue& Q, int Value);

/*
    Извлекает первый элемент очереди,
    возвращает его значение и освобождает память
*/
int Dequeue(TQueue& Q);

/*
    Выводит все элементы очереди
*/
void PrintQueue(const TQueue& Q);

/*
    Выводит адреса начала и конца очереди
*/
void PrintAddresses(const TQueue& Q);