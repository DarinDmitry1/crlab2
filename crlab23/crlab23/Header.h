#pragma once

#include <iostream>

/*
    Задача:
    Дан односвязный линейный список и указатель на его голову P1.

    Требуется:
    - вставить значение M перед каждым вторым элементом списка
    - вывести ссылку на последний элемент полученного списка P2

    Условие:
    - если количество элементов в исходном списке нечетное,
      в конец списка ничего добавлять не нужно
    - все динамические структуры должны быть реализованы через классы
    - STL использовать нельзя
    - должен быть стандартный набор методов:
        добавление
        удаление
        вывод
    - ввод данных выполняется с клавиатуры
*/

/*
    Узел односвязного списка
*/
class Node
{
public:
    int Data;
    Node* Next;

    Node(int Value);
};

/*
    Класс односвязного списка
*/
class LinkedList
{
private:
    Node* Head;

public:
    LinkedList();

    // Добавление элемента в конец списка
    void Add(int Value);

    // Удаление всех элементов списка
    void Clear();

    // Вывод списка
    void Print() const;

    // Вставка значения M перед каждым вторым элементом исходного списка
    void InsertBeforeEverySecond(int M);

    // Получить указатель на последний элемент
    Node* GetLast() const;

    // Проверка на пустоту
    bool IsEmpty() const;
};